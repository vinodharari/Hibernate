package com.vinod;
public class Person {
	private int Id;
	private String fName;
	private String lName;
	private int age;
	
	public int getId() {
		return Id;
	}
	public void setId(int id) {
		this.Id = id;
	}
	public String getfName() {
		return fName;
	}
	public void setfName(String name) {
		this.fName = name;
	}
	public String getlName() {
		return lName;
	}
	public void setlName(String lName) {
		this.lName = lName;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}	
}
